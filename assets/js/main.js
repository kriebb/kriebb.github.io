document.addEventListener("DOMContentLoaded", function() {
    // Initialize Swiper for the Projects page
    var swiper = new Swiper(".swiper", {
      // Optional parameters
      loop: true,
      pagination: {
        el: ".swiper-pagination",
        clickable: true,
      },
      navigation: {
        nextEl: ".swiper-button-next",
        prevEl: ".swiper-button-prev",
      },
      slidesPerView: 1,
      spaceBetween: 20,
      // Responsiveness
      breakpoints: {
        768: {
          slidesPerView: 2,
        },
        1024: {
          slidesPerView: 3,
        },
      },
    });
  });
  